package ru.xml.filter;

import org.w3c.dom.NodeList;

public class NodeContainer {

    private NodeList classes;

    private NodeList properties;



    public NodeList getClasses() {
        return classes;
    }

    public void setClasses(NodeList classes) {
        this.classes = classes;
    }

    public NodeList getProperties() {
        return properties;
    }

    public void setProperties(NodeList properties) {
        this.properties = properties;
    }
}
